/*************************************************************************
 * FUNCTION
 *  set_shutter_frame_length
 *
 * DESCRIPTION
 *  for frame & 3A sync
 *
 *************************************************************************/
static void set_shutter_frame_length(kal_uint32 shutter,
                     kal_uint16 frame_length)
{
    unsigned long flags;
    kal_uint16 realtime_fps = 0;
    kal_int32 dummy_line = 0;
    spin_lock_irqsave(&imgsensor_drv_lock, flags);
    imgsensor.shutter = shutter;
    spin_unlock_irqrestore(&imgsensor_drv_lock, flags);

    /* if shutter bigger than frame_length, should extend frame length first*/
    spin_lock(&imgsensor_drv_lock);
    if (frame_length > 1)
        dummy_line = frame_length - imgsensor.frame_length;

    imgsensor.frame_length = imgsensor.frame_length + dummy_line;

    if (shutter > imgsensor.frame_length - imgsensor_info.margin)
        imgsensor.frame_length = shutter + imgsensor_info.margin;

    if (imgsensor.frame_length > imgsensor_info.max_frame_length)
        imgsensor.frame_length = imgsensor_info.max_frame_length;
    spin_unlock(&imgsensor_drv_lock);
    shutter = (shutter < imgsensor_info.min_shutter)
            ? imgsensor_info.min_shutter : shutter;
    shutter =
    (shutter > (imgsensor_info.max_frame_length - imgsensor_info.margin))
        ? (imgsensor_info.max_frame_length - imgsensor_info.margin)
        : shutter;
        realtime_fps = imgsensor.pclk / imgsensor.line_length * 10 / imgsensor.frame_length;
    if (imgsensor.autoflicker_en) {
        if (realtime_fps >= 297 && realtime_fps <= 305)
            set_max_framerate(296, 0);
        else if (realtime_fps >= 147 && realtime_fps <= 150)
            set_max_framerate(146, 0);
        else
            write_cmos_sensor(0x0340, imgsensor.frame_length & 0xffff);
    } else
        write_cmos_sensor(0x0340, imgsensor.frame_length & 0xffff);
    /* Update Shutter */
    write_cmos_sensor(0x0202, shutter & 0xffff);
    pr_info(
        "Exit! shutter =%d, framelength =%d/%d, dummy_line=%d,\n",
        shutter, imgsensor.frame_length, frame_length,
        dummy_line);

}   /* set_shutter_frame_length */

static kal_uint32 gain2reg(const kal_uint16 gain)
{
     kal_uint32 reg_gain = 0x0;

    reg_gain = gain << 4;
    return (kal_uint32) reg_gain;
}

/*************************************************************************
 * FUNCTION
 *  set_gain
 *
 * DESCRIPTION
 *  This function is to set global gain to sensor.
 *
 * PARAMETERS
 *  iGain : sensor global gain(base: 0x40)
 *
 * RETURNS
 *  the actually gain set to sensor.
 *
 * GLOBALS AFFECTED
 *
 *************************************************************************/
static kal_uint16 set_gain(kal_uint16 gain)
{
    kal_uint32 reg_gain = 0;
    kal_uint16 a = 0 ,  b = 0;

    if (gain < imgsensor_info.min_gain || gain > imgsensor_info.max_gain) {
        pr_info("Error max gain setting: %d\n", gain);

        if (gain < imgsensor_info.min_gain)
            gain = imgsensor_info.min_gain;
        else if (gain > imgsensor_info.max_gain)
            gain = imgsensor_info.max_gain;
    }

    reg_gain = gain2reg(gain);
    spin_lock(&imgsensor_drv_lock);
    imgsensor.gain = reg_gain;
    spin_unlock(&imgsensor_drv_lock);
    pr_info("gain = %d, reg_gain = 0x%x\n ",
        gain, reg_gain);
    write_cmos_sensor(0x0204, reg_gain & 0xffff);
    a = read_cmos_sensor_8(0x0204);
    b = read_cmos_sensor_8(0x0205);
    pr_info(" 0x204 = 0x%x , 0x205 = 0x%x , 0x206 = 0x%x \n", a, b);
    return gain;
} /* set_gain */
