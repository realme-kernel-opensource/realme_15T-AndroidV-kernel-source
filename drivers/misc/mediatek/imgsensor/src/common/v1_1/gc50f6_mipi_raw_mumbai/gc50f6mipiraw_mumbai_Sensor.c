#define GC50F6_MUMBAI_EEPROM_I2C_ADDR 0xA0
#define GC50F6_ECO_VALUE 3
static BYTE gc50f6_mumbai_common_data[CAMERA_EEPPROM_COMDATA_LENGTH] = { 0 };

static kal_uint16 read_gc50f6_mumbai_eeprom_module(kal_uint32 addr)
{
    kal_uint16 get_byte = 0;
    char pusendcmd[2] = { (char)(addr >> 8), (char)(addr & 0xFF) };

    iReadRegI2C(pusendcmd, 2, (u8 *) &get_byte, 1, GC50F6_MUMBAI_EEPROM_I2C_ADDR);

    return get_byte;
}

static void read_gc50f6_mumbai_module_data()
{
    kal_uint16 idx = 0;
    kal_uint16 sn_length = 23;
    kal_uint32 sn_starAddr = 0x50;
    kal_uint32 vcmAddr = 0x0A;
    kal_uint32 lensAddr = 0x08;
    kal_uint32 macAddr_lsb = 0x44;
    kal_uint32 macAddr_msb = 0x45;
    kal_uint32 infAddr_lsb = 0x46;
    kal_uint32 infAddr_msb = 0x47;

    memset(gc50f6_mumbai_common_data, 0,sizeof(gc50f6_mumbai_common_data));
    // QR
    for(idx = 0; idx < sn_length; idx++)
    {
        gc50f6_mumbai_common_data[8 + idx] = read_gc50f6_mumbai_eeprom_module(sn_starAddr + idx);
    }
    // vcm
    gc50f6_mumbai_common_data[40] = read_gc50f6_mumbai_eeprom_module(vcmAddr);
    gc50f6_mumbai_common_data[41] = read_gc50f6_mumbai_eeprom_module(vcmAddr + 1);
     // lensid
    gc50f6_mumbai_common_data[44] = read_gc50f6_mumbai_eeprom_module(lensAddr);
    gc50f6_mumbai_common_data[45] = read_gc50f6_mumbai_eeprom_module(lensAddr + 1);
    // macro
    gc50f6_mumbai_common_data[48] = read_gc50f6_mumbai_eeprom_module(macAddr_lsb);
    gc50f6_mumbai_common_data[49] = read_gc50f6_mumbai_eeprom_module(macAddr_msb);
    // inf
    gc50f6_mumbai_common_data[52] = read_gc50f6_mumbai_eeprom_module(infAddr_lsb);
    gc50f6_mumbai_common_data[53] = read_gc50f6_mumbai_eeprom_module(infAddr_msb);

    for (idx = 0; idx < CAMERA_EEPPROM_COMDATA_LENGTH; idx = idx + 4)
        printk("cam data: %02x %02x %02x %02x\n",
               gc50f6_mumbai_common_data[idx],
               gc50f6_mumbai_common_data[idx + 1],
               gc50f6_mumbai_common_data[idx + 2],
               gc50f6_mumbai_common_data[idx + 3]);
}

static void read_cmos_eeprom_table(kal_uint16 addr, kal_uint8 *table, kal_uint32 size)
{
    char pusendcmd[2] = {(char)(addr >> 8), (char)(addr & 0xFF) };
    iReadRegI2C(pusendcmd, 2, (u8 *)table, size, 0xA0);
}

static kal_uint16 read_otp_info(kal_uint8 *data)
{
    kal_uint16 addr = 0x00;
    kal_uint16 left_size = 0;

    left_size = 0x4000;
    while(left_size > 0){
        if (left_size >= 1024) {
            read_cmos_eeprom_table(addr,data, 1024);
            left_size = left_size - 1024;
        }else {
            read_cmos_eeprom_table(addr,data, left_size);
            break;
        }
        addr = addr + 1024;
        data = data + 1024;
    }
    return 0;
}

