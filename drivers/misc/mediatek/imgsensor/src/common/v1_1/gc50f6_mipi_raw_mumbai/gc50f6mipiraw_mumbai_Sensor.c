static kal_uint8 ct_read_data[CROSSTALK_BUF_SIZE];

void gc50f6_read_crosstalk_data(void)
{
	kal_uint16 i = 0;
	kal_uint16 ct_flag = 0;
	kal_uint32 checksum = 0;
    pr_debug("start read crosstalk");
	memset(&ct_read_data, 0, CROSSTALK_BUF_SIZE * sizeof(kal_uint8));

	ct_flag = read_eeprom(CROSSTALK_FLAG_OFFSET);
	pr_debug("ct_flag = %d\n", ct_flag);
//	ct_flag = 1;
	if (ct_flag == OTP_FLAG_VALID) {
		for (i = 0; i < CROSSTALK_BUF_SIZE; i++)
			ct_read_data[i] = read_eeprom(CROSSTALK_START_ADDR + i);

		for (i = 0; i < (CROSSTALK_BUF_SIZE - 1); i++)
			checksum += ct_read_data[i];

		//checksum++;

		if (((checksum % 255)+1) == ct_read_data[CROSSTALK_BUF_SIZE - 1])
			pr_debug("check success! calc_checksum = %d, read_checksum = %d\n",
			(checksum % 255)+1, ct_read_data[CROSSTALK_BUF_SIZE - 1]);
		else {
			memset(ct_read_data, 0, CROSSTALK_BUF_SIZE);
			pr_debug("check error! calc_checksum = %d, read_checksum = %d\n",
			((checksum % 255)+1), ct_read_data[CROSSTALK_BUF_SIZE - 1]);
		}
#if CT_DEBUG
		for (i = 0; i < CROSSTALK_BUF_SIZE; i++)
			pr_debug("ct_data[%d] = %x\n", i, ct_read_data[i]);
#endif
	} else
		pr_debug("crosstalk data is zero!\n");
}

void gc50f6_write_crosstalk_data(void)
{
	kal_uint32 i = 0, page = 0, j = 0;
	kal_uint32 start_page = 0x06, start_addr = 0x0600;

	write_cmos_sensor_8(0x031c, 0x60);
	write_cmos_sensor_8(0x0004, 0x06);
	write_cmos_sensor_8(0x001f, 0x01);
	mdelay(5);
	pr_debug("start write crosstalk_data!\n");
	for (page = 0;  page < 2; page++)
	{
		write_cmos_sensor_8(0x0004, start_page+0x10*page);
		for (i = 0; i < 128 && j < CROSSTALK_BUF_SIZE - 1 ; i++)
		{
			write_cmos_sensor(start_addr+2*i, ((ct_read_data[j] << 8) & 0xFF00)|ct_read_data[j+1]);
			j=j+2; 
			pr_debug("XTCdata = 0x%x\n",read_cmos_sensor_8(start_addr+2*i));
			pr_debug("XTCdata = 0x%x\n",read_cmos_sensor_8(start_addr+2*i+1));
		}
	pr_debug("page = %x\n", read_cmos_sensor_8(0x0004) );
	}
	write_cmos_sensor_8(0x0004, 0x07);
	write_cmos_sensor_8(0x031c, 0x9b);

	pr_debug("sram_i = %d\n", i );
	pr_debug("CTdata_j = %d\n", j );
	pr_debug("end write crosstalk_data!\n");

}

/*************************************************************************
 * FUNCTION
 *  get_imgsensor_id
 *
 * DESCRIPTION
 *  This function get the sensor ID
 *
 * PARAMETERS
 *  *sensorID : return the sensor ID
 *
 * RETURNS
 *  None
 *
 * GLOBALS AFFECTED
 *
 *************************************************************************/
static kal_uint32 get_imgsensor_id(UINT32 *sensor_id)
{
    kal_uint8 i = 0;
    kal_uint8 retry = 2;
    /* sensor have two i2c address 0x62 & 0x20,
     * we should detect the module used i2c address
     */
    while (imgsensor_info.i2c_addr_table[i] != 0xff) {
        spin_lock(&imgsensor_drv_lock);
        imgsensor.i2c_write_id = imgsensor_info.i2c_addr_table[i];
        spin_unlock(&imgsensor_drv_lock);
        do {
            *sensor_id = (return_sensor_id()-0x1EA5);
            pr_info("phl sensor_id=%x\n", *sensor_id);
            if (*sensor_id == imgsensor_info.sensor_id) {
                pr_info("phl *sensor_id == imgsensor_info.sensor_id");
                if ((read_gc50f6_mumbai_eeprom_module(0x00)) == imgsensor_info.module_id) {
                    read_gc50f6_version();
                    if(deviceInfo_register_value == 0x00) {
                        register_imgsensor_deviceinfo("Cam_f", DEVICE_VERSION_MUMBAI_GC50F6, imgsensor_info.module_id);
                        read_gc50f6_mumbai_module_data();
                        if(otp_data[0] == 0) {
                            read_otp_info(otp_data);
                        } else {
                            pr_err("otp data has already read");
                        }
                        deviceInfo_register_value = 0x01;
                        }
                    gc50f6_read_crosstalk_data();
                } else {
                    *sensor_id = 0xFFFFFFFF;
                    printk("gc50f6_camera_sensor get_imgsensor_id failed: 0x%x\n", *sensor_id);
                    return ERROR_SENSOR_CONNECT_FAIL;
                }
            printk("gc50f6_camera_sensor get_imgsensor_id success: 0x%x\n", *sensor_id);
            return ERROR_NONE;
            }
            printk("[gc50f6_camera_sensor]get_imgsensor_id:Read sensor id fail, write id: 0x%x, id: 0x%x\n",
                imgsensor.i2c_write_id, *sensor_id);
            retry--;
        } while (retry > 0);
        i++;
        retry = 2;
    }
    if (*sensor_id != imgsensor_info.sensor_id) {
        /* if Sensor ID is not correct,
         * Must set *sensor_id to 0xFFFFFFFF
         */
        *sensor_id = 0xFFFFFFFF;
        return ERROR_SENSOR_CONNECT_FAIL;
    }
    return ERROR_NONE;
}

/*************************************************************************
 * FUNCTION
 *  open
 *
 * DESCRIPTION
 *  This function initialize the registers of CMOS sensor
 *
 * PARAMETERS
 *  None
 *
 * RETURNS
 *  None
 *
 * GLOBALS AFFECTED
 *
 *************************************************************************/
static kal_uint32 open(void)
{
    kal_uint8 i = 0;
    kal_uint8 retry = 2;
    kal_uint16 sensor_id = 0;


    while (imgsensor_info.i2c_addr_table[i] != 0xff) {
        spin_lock(&imgsensor_drv_lock);
        imgsensor.i2c_write_id = imgsensor_info.i2c_addr_table[i];
        spin_unlock(&imgsensor_drv_lock);
        do {
            sensor_id = (return_sensor_id()-0X1EA5);
            if (sensor_id == imgsensor_info.sensor_id) {
                pr_info("[gc50f6_camera_sensor]open:i2c write id: 0x%x, sensor id: 0x%x\n",
                    imgsensor.i2c_write_id, sensor_id);
                break;
            }
            pr_info("[gc50f6_camera_sensor]open:Read sensor id fail, write id: 0x%x, id: 0x%x\n",
                imgsensor.i2c_write_id, sensor_id);
            retry--;
        } while (retry > 0);
        i++;
        if (sensor_id == imgsensor_info.sensor_id)
            break;
        retry = 2;
    }
    if (imgsensor_info.sensor_id != sensor_id)
        return ERROR_SENSOR_CONNECT_FAIL;

    /* initail sequence write in  */
    sensor_init();

    gc50f6_write_crosstalk_data();

    spin_lock(&imgsensor_drv_lock);

    imgsensor.autoflicker_en = KAL_FALSE;
    imgsensor.sensor_mode = IMGSENSOR_MODE_INIT;
    imgsensor.shutter = 0x480;
    imgsensor.gain = 0x40;
    imgsensor.pclk = imgsensor_info.pre.pclk;
    imgsensor.frame_length = imgsensor_info.pre.framelength;
    imgsensor.line_length = imgsensor_info.pre.linelength;
    imgsensor.min_frame_length = imgsensor_info.pre.framelength;
    imgsensor.dummy_pixel = 0;
    imgsensor.dummy_line = 0;
    imgsensor.ihdr_mode = 0;
    imgsensor.test_pattern = KAL_FALSE;
    imgsensor.current_fps = imgsensor_info.pre.max_framerate;
    imgsensor.pdaf_mode = 0;
    spin_unlock(&imgsensor_drv_lock);
    pr_info("%s -\n", __func__);

    return ERROR_NONE;
}

